PILLS_REPO="https://github.com/NixOS/nix-pills.git"
RELEASE_REPO="https://github.com/NixOS/release-wiki.git"

cleanup() { rm -rf "$WORK_DIR"; }
trap cleanup EXIT

# ── Clone all sources ──────────────────────────────────────────────────

echo "==> Cloning source repositories (parallel)..."

git clone --depth 1 --quiet "$NIXDEV_REPO" "$WORK_DIR/nix.dev" &
PID_NIXDEV=$!

git clone --depth 1 --quiet "$PILLS_REPO" "$WORK_DIR/nix-pills" &
PID_PILLS=$!

git clone --depth 1 --quiet "$RELEASE_REPO" "$WORK_DIR/release-wiki" &
PID_RELEASE=$!

# Sparse checkout for nixpkgs (only nixos/doc/manual/)
(
    git clone --depth 1 --filter=blob:none --sparse --quiet "$NIXPKGS_REPO" "$WORK_DIR/nixpkgs"
    git -C "$WORK_DIR/nixpkgs" sparse-checkout set nixos/doc/manual/
) &
PID_NIXPKGS=$!

wait $PID_NIXDEV $PID_PILLS $PID_RELEASE $PID_NIXPKGS
echo "    All repos cloned."

# ── Collect version info ───────────────────────────────────────────────

NIXDEV_COMMIT=$(git -C "$WORK_DIR/nix.dev" rev-parse --short HEAD)
NIXDEV_DATE=$(git -C "$WORK_DIR/nix.dev" log -1 --format=%ci)
NIXPKGS_COMMIT=$(git -C "$WORK_DIR/nixpkgs" rev-parse --short HEAD)
NIXPKGS_DATE=$(git -C "$WORK_DIR/nixpkgs" log -1 --format=%ci)
PILLS_COMMIT=$(git -C "$WORK_DIR/nix-pills" rev-parse --short HEAD)
PILLS_DATE=$(git -C "$WORK_DIR/nix-pills" log -1 --format=%ci)
RELEASE_COMMIT=$(git -C "$WORK_DIR/release-wiki" rev-parse --short HEAD)
RELEASE_DATE=$(git -C "$WORK_DIR/release-wiki" log -1 --format=%ci)

echo "==> Source versions:"
echo "    nix.dev:      $NIXDEV_COMMIT ($NIXDEV_DATE)"
echo "    nixpkgs:      $NIXPKGS_COMMIT ($NIXPKGS_DATE)"
echo "    nix-pills:    $PILLS_COMMIT ($PILLS_DATE)"
echo "    release-wiki: $RELEASE_COMMIT ($RELEASE_DATE)"

# Shorthand paths
NIXDEV="$WORK_DIR/nix.dev/source"
MANUAL="$WORK_DIR/nixpkgs/nixos/doc/manual"
PILLS="$WORK_DIR/nix-pills/pills"
RELEASE="$WORK_DIR/release-wiki/src"

mkdir -p "$REFS_DIR"

# ── Helpers ────────────────────────────────────────────────────────────

# copy_page <source_file> <output_filename> <source_url> <repo_label>
copy_page() {
    local src="$1" dst="$REFS_DIR/$2" url="$3" repo="$4"
    if [[ ! -f "$src" ]]; then
        echo "  WARN: $(basename "$src") not found, skipping"
        return
    fi
    {
        echo "<!-- source: $url -->"
        echo "<!-- repo: $repo -->"
        echo "<!-- auto-generated by scripts/generate-references.sh - DO NOT EDIT MANUALLY -->"
        echo ""
        cat "$src"
    } > "$dst"
    echo "  OK: $2"
}

# merge_pages <output_filename> <header> <source_url> <repo_label> <file1> [file2...]
merge_pages() {
    local dst="$REFS_DIR/$1" header="$2" url="$3" repo="$4"
    shift 4
    {
        echo "<!-- source: $url -->"
        echo "<!-- repo: $repo -->"
        echo "<!-- auto-generated by scripts/generate-references.sh - DO NOT EDIT MANUALLY -->"
        echo ""
        echo "# $header"
        echo ""
        for src in "$@"; do
            if [[ -f "$src" ]]; then
                cat "$src"
                echo ""
                echo "---"
                echo ""
            else
                echo "  WARN: $(basename "$src") not found, skipping from merge" >&2
            fi
        done
    } > "$dst"
    echo "  OK: $1 (merged)"
}

